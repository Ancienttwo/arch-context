import { describe, expect, test } from "bun:test";
import { architectureEventHash, digestJson, type Json } from "@archcontext/contracts";
import {
  architectureLedgerStateDigest,
  planYamlToArchitectureLedgerImport,
  projectArchitectureLedgerStateToYamlFiles,
  type ArchitectureLedgerModelFile,
  type ArchitectureLedgerScope
} from "../src/index";

const scope: ArchitectureLedgerScope = {
  repository: {
    repositoryId: "repo.yaml-ledger",
    storageRepositoryId: "repo.storage.yaml-ledger"
  },
  worktree: {
    workspaceId: "workspace.yaml-ledger",
    storageWorkspaceId: "workspace.storage.yaml-ledger",
    branch: "main",
    headSha: "abc123yaml",
    worktreeDigest: digestJson({ worktree: "yaml-ledger" } as unknown as Json)
  }
};

describe("@archcontext/core/architecture-ledger YAML bridge", () => {
  test("plans deterministic YAML import and projects back with zero semantic drift", () => {
    const files = [
      modelFile(".archcontext/model/nodes/module.api.yaml", [
        "schemaVersion: \"archcontext.node/v1\"",
        "id: \"module.api\"",
        "kind: \"module\"",
        "name: \"API\"",
        "status: \"active\"",
        "summary: \"Serves checkout requests.\"",
        ""
      ]),
      modelFile(".archcontext/model/nodes/module.checkout.yaml", [
        "schemaVersion: \"archcontext.node/v1\"",
        "id: \"module.checkout\"",
        "kind: \"module\"",
        "name: \"Checkout\"",
        "status: \"active\"",
        "summary: \"Owns checkout orchestration.\"",
        ""
      ]),
      modelFile(".archcontext/model/relations/relation.checkout-api.yaml", [
        "schemaVersion: \"archcontext.relation/v1\"",
        "id: \"relation.checkout-api\"",
        "kind: \"calls\"",
        "source: \"module.checkout\"",
        "target: \"module.api\"",
        "intent: \"Checkout calls API.\"",
        ""
      ]),
      modelFile(".archcontext/model/constraints/constraint.api-owner.yaml", [
        "schemaVersion: \"archcontext.constraint/v1\"",
        "id: \"constraint.api-owner\"",
        "kind: \"owner-required\"",
        "subject: \"module.api\"",
        "severity: \"warning\"",
        "summary: \"API must have an owner.\"",
        ""
      ]),
      modelFile(".archcontext/manifest.yaml", [
        "schemaVersion: \"archcontext.manifest/v1\"",
        "product:",
        "  id: \"product.checkout\"",
        "  name: \"Checkout\"",
        ""
      ]),
      modelFile(".archcontext/policies/review.yaml", [
        "schemaVersion: \"archcontext.policy/v1\"",
        "id: \"policy.review\"",
        "failOn:",
        "  - \"stale-context\"",
        ""
      ]),
      modelFile(".archcontext/generated/ARCHITECTURE.md", "<!-- Generated by ArchContext. Do not edit by hand. -->\n")
    ];

    const plan = planYamlToArchitectureLedgerImport({
      ...scope,
      files: [...files].reverse(),
      createdAt: "2026-06-25T02:00:00.000Z"
    });
    const repeated = planYamlToArchitectureLedgerImport({
      ...scope,
      files,
      createdAt: "2026-06-25T02:00:00.000Z"
    });

    expect(plan.graphDigest).toBe(repeated.graphDigest);
    expect(plan.sourceDigest).toBe(repeated.sourceDigest);
    expect(plan.event.eventHash).toBe(architectureEventHash(plan.event));
    expect(plan.state.entities.map((entity) => entity.entityId)).toEqual(["module.api", "module.checkout"]);
    expect(plan.state.relations.map((relation) => relation.relationId)).toEqual(["relation.checkout-api"]);
    expect(plan.state.constraints.map((constraint) => constraint.constraintId)).toEqual(["constraint.api-owner"]);
    expect(plan.imported).toContainEqual(expect.objectContaining({
      path: ".archcontext/policies/review.yaml",
      targetKind: "evidence"
    }));
    expect(plan.ignoredFiles).toEqual([{ path: ".archcontext/generated/ARCHITECTURE.md", reasonCode: "generated-projection" }]);
    expect(plan.unsupportedFiles).toEqual([]);
    expect(plan.drift).toMatchObject({ ok: true, semanticDrift: false, reasonCodes: [] });
    expect(plan.projectedFiles.map((file) => file.path)).toEqual([
      ".archcontext/model/constraints/constraint.api-owner.yaml",
      ".archcontext/model/nodes/module.api.yaml",
      ".archcontext/model/nodes/module.checkout.yaml",
      ".archcontext/model/relations/relation.checkout-api.yaml"
    ]);
    expect(architectureLedgerStateDigest(plan.state)).toBe(plan.drift.projectedGraphDigest);
    expect(projectArchitectureLedgerStateToYamlFiles(plan.state)[0]!.body).toContain("schemaVersion: \"archcontext.constraint/v1\"");
  });

  test("reports unsupported authoritative YAML without mutating supported graph facts", () => {
    const plan = planYamlToArchitectureLedgerImport({
      ...scope,
      createdAt: "2026-06-25T02:00:00.000Z",
      files: [
        modelFile(".archcontext/model/nodes/module.api.yaml", [
          "schemaVersion: \"archcontext.node/v1\"",
          "id: \"module.api\"",
          "kind: \"module\"",
          "name: \"API\"",
          ""
        ]),
        modelFile(".archcontext/model/custom/unknown.yaml", [
          "schemaVersion: \"archcontext.custom/v1\"",
          "id: \"custom.one\"",
          ""
        ])
      ]
    });

    expect(plan.state.entities.map((entity) => entity.entityId)).toEqual(["module.api"]);
    expect(plan.unsupportedFiles).toEqual([{
      path: ".archcontext/model/custom/unknown.yaml",
      schemaVersion: "archcontext.custom/v1",
      reasonCode: "unsupported-schema",
      message: ".archcontext/model/custom/unknown.yaml: unsupported architecture ledger YAML schema archcontext.custom/v1"
    }]);
    expect(plan.drift.ok).toBe(false);
    expect(plan.drift.reasonCodes).toEqual(["unsupported-yaml-file"]);
  });

  test("reports invalid authoritative YAML records without aborting dry-run import", () => {
    const plan = planYamlToArchitectureLedgerImport({
      ...scope,
      createdAt: "2026-06-25T02:00:00.000Z",
      files: [
        modelFile(".archcontext/model/nodes/module.api.yaml", [
          "schemaVersion: \"archcontext.node/v1\"",
          "id: \"module.api\"",
          "kind: \"module\"",
          "name: \"API\"",
          ""
        ]),
        modelFile(".archcontext/model/relations/relation.invalid.yaml", [
          "schemaVersion: \"archcontext.relation/v1\"",
          "id: \"relation.invalid\"",
          "kind: \"calls\"",
          "source: \"module.api\"",
          ""
        ])
      ]
    });

    expect(plan.state.entities.map((entity) => entity.entityId)).toEqual(["module.api"]);
    expect(plan.state.relations).toEqual([]);
    expect(plan.unsupportedFiles).toEqual([{
      path: ".archcontext/model/relations/relation.invalid.yaml",
      schemaVersion: "archcontext.relation/v1",
      reasonCode: "invalid-record",
      message: ".archcontext/model/relations/relation.invalid.yaml: target is required"
    }]);
    expect(plan.drift.ok).toBe(false);
    expect(plan.drift.reasonCodes).toEqual(["unsupported-yaml-file"]);
  });
});

function modelFile(path: string, lines: string[] | string): ArchitectureLedgerModelFile {
  const body = Array.isArray(lines) ? lines.join("\n") : lines;
  return {
    path,
    body,
    digest: digestJson({ path, body } as unknown as Json)
  };
}
